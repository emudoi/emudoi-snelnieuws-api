package com.snelnieuws.model

/** Backend-driven UI translations for the iOS + Android clients.
  *
  * Same shape as [[CategoryNames]] — a `Map[locale, Map[key, value]]`
  * with the English row as the source of truth and authoritative
  * fallback for unknown locales. Apps call `GET /v3/i18n?language=xx`
  * once on startup and on every language switch, cache the response,
  * and look up strings via `L10n.t("key")` instead of literal text.
  *
  * Adding a new language tomorrow:
  *   1. Add a new entry under `strings` for the locale code
  *   2. Make sure `Languages.codes` includes it (so the picker shows it)
  *   3. Deploy. Apps pick it up on next launch — no app release needed.
  *
  * Adding a new key:
  *   1. Add it under the "en" row (the source of truth)
  *   2. Add it under every other locale row, or leave it out and the
  *      English value is used as fallback at lookup time
  *   3. Mirror the same key in the apps' bundled fallback resources
  *      (`Localizable.strings` on iOS, `strings.xml` on Android) so
  *      offline / cache-cold launches still render usable text
  *
  * The `forLocale(locale)` resolver returns the inner map for the
  * requested locale, falling back to the English row for unknown
  * locales — same semantics as `CategoryNames.forLocale`.
  *
  * Per-locale Maps below are kept under ~200 keys (UI chrome only). If
  * we ever want copy-A/B testing or non-engineer-editable translations,
  * migrate to a DB-backed schema at that point — but the current scale
  * (~7 languages × ~150 keys) fits comfortably in process memory.
  */
