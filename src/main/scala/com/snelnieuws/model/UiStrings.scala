    "alert.deleteAccount.failed"    -> "Nie udało się usunąć konta",
  )

  val strings: Map[String, Map[String, String]] = Map(
    "en" -> en,
    "nl" -> nl,
    "de" -> de,
    "fr" -> fr,
    "it" -> it,
    "es" -> es,
    "pl" -> pl
  )

  /** Resolve the i18n payload for a caller's locale. Unknown locales
    * fall back to the English row. Mirrors `CategoryNames.forLocale`
    * semantics. The client-side `L10n` helper does its own per-key
    * English fallback for any key the caller's locale row omits.
    */
  def forLocale(locale: String): Map[String, String] =
    strings.getOrElse(locale, en)
}
